# Functions, shown by def function_name():
# Functions need indentation just like loops
def add(a, b):
    addition = a + b
    print(f"The total sum is {addition}")  # A function that returns a sum of two numbers


def sub(a, b):
    subtraction = a - b
    print(f"The difference of the numbers is: {subtraction}")
    # A function that returns the difference between two numbers


def mul(a, b):
    multiplication = a * b
    print(f"The product of the numbers is: {multiplication}")  # A function that returns the product of two numbers


def div(a, b):
    division = a / b
    print(f"The total quotient is: {division}")  # A function that returns the quotient of two numbers


def modu(a, b):
    modulus = a % b
    print(f"The remainder of the numbers is: {modulus}")
    # A function that returns the remainder of the quotient two numbers


def powe(a, b):
    power = a ** b
    print(f"The power of the number is: {power}")  # A function that returns the power of a number


